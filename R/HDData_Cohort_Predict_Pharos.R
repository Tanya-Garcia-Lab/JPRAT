#'   \item{hdage_nobase}{ages when HD diagnosis has occurred for subjects who did not have HD diagnosis at the baseline.}
#'   \item{mcione}{ages when the first multi-domain cognitive impairments has occurred for subjects.}
#'   \item{dep2}{ages when depression were occurred for subjects.}
#'   \item{delta.hdage_nobase}{censoring indicator for individuals who entered the study without a diagnosis (1 if a person had a HD diagnosis, 0 otherwise).}
